s = "Hello"
# Index 3 is the letter "l"
print(s[3])
# Arithmetic operations on strings
print(s + " World!")
# Repeat the string 3 times
print("la" * 3)
# Slicing
# Get the first three characters
print(s[:3])
# Get the last two characters
print(s[-2:])
# Get the middle two characters
print(s[1:3])

# Common string operations
# String length
print(len(s))
# String concatenation
print(s + " World!")
# Repeat the string 3 times
print(s * 3)
# Other basic operations
# Upper case
print(s.upper())
# Lower case
print(s.lower())
# Capitalize
print(s.capitalize())
# Replace
print(s.replace("Hello", "Hi"))