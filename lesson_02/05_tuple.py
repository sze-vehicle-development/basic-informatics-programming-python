# Tuples
t = (1, 2, 3)
print(t)
# Add two tuples
t1 = (4, 5, 6)
print(t + t1)
# Repeat the tuple
print(t * 3)
# Slicing
# Get the second element
print(t[1])
# Get the first two elements
print(t[:2])
# Get the last two elements
print(t[-2:])
# Get the middle two elements
print(t[1:3])
# Common tuple operations
# Tuple length
print(len(t))
# Aggregation operations
# Sum
print("Sum: ", sum(t))
# Minimum
print("Minimum: ", min(t))
# Maximum
print("Maximum: ", max(t))
# Average
print("Average: ", sum(t) / len(t))
